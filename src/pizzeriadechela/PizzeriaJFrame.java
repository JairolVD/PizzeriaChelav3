    private void _ingresarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event__ingresarButtonMouseClicked
        // TODO add your handling code here:
        enableInicioSesion(false);
        if (!Utilidad.validarDNI(_dniTextField.getText())){
            JOptionPane.showMessageDialog(null, "El DNI ingresado no es v치lido.", "Ocurrio un error", JOptionPane.ERROR_MESSAGE);
            enableInicioSesion(true);
            return;
        }
        
        String contrasenia = Utilidad.getCadena(_contraTextField.getPassword());
        
        if (!Utilidad.validarContrasenia(contrasenia)){
            JOptionPane.showMessageDialog(null, "La contrase침a ingresada no es valida.", "Ocurrio un error", JOptionPane.ERROR_MESSAGE);
            enableInicioSesion(true);
            return;
        }
        
        CuentaDto cuenta = _mysqlAction.getCuenta(_dniTextField.getText());
        if (cuenta == null){
            JOptionPane.showMessageDialog(null, "La cuenta con el dni ingresado no existe.", "Ocurrio un error", JOptionPane.ERROR_MESSAGE);
            enableInicioSesion(true);
            return;
        }
        
        if (!Utilidad.validarContrasenias(cuenta.getContrasenia(), contrasenia)){
            JOptionPane.showMessageDialog(null, "La contrase침a es incorrecta.", "Ocurrio un error", JOptionPane.ERROR_MESSAGE);
            enableInicioSesion(true);
            return;
        }
        
        if (cuenta.getCargo() < 1 || cuenta.getCargo() == 2){
            JOptionPane.showMessageDialog(null, "La cuenta no tiene acceso autorizado.", "Ocurrio un error", JOptionPane.ERROR_MESSAGE);
            enableInicioSesion(true);
            return;
        }
        
        PizzeriaDChelaFrame frame = new PizzeriaDChelaFrame();
                frame.initializate(this, cuenta, _mysqlAction)
                     .setVisible(true);
                ocultarVentana();
    }//GEN-LAST:event__ingresarButtonMouseClicked

